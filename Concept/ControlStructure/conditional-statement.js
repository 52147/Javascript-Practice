// if, else if, else
// for branching logic based on conditions.
let x = 10;

if (x > 10) {
    console.log("x is greater than 10");
} else if (x === 10) {
    console.log("x is equal to 10");
} else {
    console.log("x is less than 10");
}

// switch
// branching logic based on different cases.
let color = "red";

switch (color) {
    case "red":
        console.log("The color is red");
        break;
    case "blue":
        console.log("The color is blue");
        break;
    case "green":
        console.log("The color is green");
        break;
    default:
        console.log("Unknown color");
}
