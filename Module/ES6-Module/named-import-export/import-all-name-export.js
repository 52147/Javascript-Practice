// Importing All Exports

// You can import all exports from a module using the * syntax, which is useful when you need to import multiple named exports under a single namespace.

// Example:

// main.js

// Importing all named exports under a namespace
import * as math from './math.js';

console.log(math.add(2, 3));       // Output: 5
console.log(math.subtract(5, 3));  // Output: 2
